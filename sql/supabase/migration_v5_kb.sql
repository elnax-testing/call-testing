-- Knowledge base + Lead RRat CRM ingestion schema
-- Safe to re-run.

CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = timezone('utc', now());
    RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS kb_sources (
    id             bigint generated by default as identity PRIMARY KEY,
    created_at     timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    updated_at     timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    source_type    text NOT NULL,
    title          text NOT NULL DEFAULT '',
    source_url     text,
    raw_text       text,
    storage_bucket text,
    storage_path   text,
    mime_type      text,
    checksum       text,
    status         text NOT NULL DEFAULT 'pending',
    enabled        boolean NOT NULL DEFAULT true,
    last_synced_at timestamptz,
    sync_error     text,
    metadata       jsonb NOT NULL DEFAULT '{}'::jsonb,
    CONSTRAINT kb_sources_type_check CHECK (source_type IN ('pdf_upload', 'web_url', 'text_note', 'leadrat_crm')),
    CONSTRAINT kb_sources_status_check CHECK (status IN ('pending', 'ready', 'syncing', 'error', 'disabled'))
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_kb_sources_leadrat_unique
    ON kb_sources (source_type)
    WHERE source_type = 'leadrat_crm';

CREATE INDEX IF NOT EXISTS idx_kb_sources_type_status
    ON kb_sources (source_type, status);

DROP TRIGGER IF EXISTS set_kb_sources_updated_at ON kb_sources;
CREATE TRIGGER set_kb_sources_updated_at
BEFORE UPDATE ON kb_sources
FOR EACH ROW
EXECUTE FUNCTION public.set_updated_at();

ALTER TABLE kb_sources ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow kb sources all" ON kb_sources;
CREATE POLICY "Allow kb sources all"
    ON kb_sources FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);


