INSERT INTO storage.buckets (id, name, public)
VALUES ('kb-files', 'kb-files', true)
ON CONFLICT (id) DO UPDATE SET public = true;

DROP POLICY IF EXISTS "Public read kb files" ON storage.objects;
CREATE POLICY "Public read kb files"
    ON storage.objects
    FOR SELECT
    USING (bucket_id = 'kb-files');

DROP POLICY IF EXISTS "Allow kb files upload" ON storage.objects;
CREATE POLICY "Allow kb files upload"
    ON storage.objects
    FOR INSERT TO anon, authenticated
    WITH CHECK (bucket_id = 'kb-files');
