-- Safe to re-run.

CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = timezone('utc', now());
    RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS wa_conversations (
    id                   bigint generated by default as identity PRIMARY KEY,
    created_at           timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    updated_at           timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    phone_number         text NOT NULL,
    display_name         text,
    last_message_preview text,
    last_message_at      timestamptz,
    unread_count         integer DEFAULT 0 NOT NULL,
    provider             text DEFAULT 'baileys' NOT NULL,
    session_name         text DEFAULT 'primary' NOT NULL,
    metadata             jsonb DEFAULT '{}'::jsonb NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_wa_conversations_phone
    ON wa_conversations (phone_number);

DROP TRIGGER IF EXISTS set_wa_conversations_updated_at ON wa_conversations;
CREATE TRIGGER set_wa_conversations_updated_at
BEFORE UPDATE ON wa_conversations
FOR EACH ROW
EXECUTE FUNCTION public.set_updated_at();

ALTER TABLE wa_conversations ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow wa conversations all" ON wa_conversations;
CREATE POLICY "Allow wa conversations all"
    ON wa_conversations FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);


