CREATE TABLE IF NOT EXISTS wa_messages (
    id                     bigint generated by default as identity PRIMARY KEY,
    created_at             timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    conversation_id        bigint REFERENCES wa_conversations(id) ON DELETE SET NULL,
    phone_number           text NOT NULL,
    direction              text NOT NULL,
    status                 text DEFAULT 'queued' NOT NULL,
    message_type           text DEFAULT 'text' NOT NULL,
    template_name          text,
    body_text              text DEFAULT '' NOT NULL,
    caption                text DEFAULT '' NOT NULL,
    media_url              text,
    mime_type              text,
    file_name              text,
    asset_id               bigint,
    provider               text DEFAULT 'baileys' NOT NULL,
    provider_message_id    text,
    error_text             text,
    related_appointment_id bigint,
    related_job_id         bigint REFERENCES automation_jobs(id) ON DELETE SET NULL,
    metadata               jsonb DEFAULT '{}'::jsonb NOT NULL,
    sent_at                timestamptz,
    delivered_at           timestamptz,
    read_at                timestamptz,
    CONSTRAINT wa_messages_direction_check CHECK (direction IN ('inbound', 'outbound')),
    CONSTRAINT wa_messages_type_check CHECK (message_type IN ('text', 'image', 'document', 'audio'))
);

CREATE INDEX IF NOT EXISTS idx_wa_messages_phone
    ON wa_messages (phone_number, created_at);
CREATE UNIQUE INDEX IF NOT EXISTS idx_wa_messages_provider_message
    ON wa_messages (provider_message_id)
    WHERE provider_message_id IS NOT NULL;

ALTER TABLE wa_messages ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow wa messages all" ON wa_messages;
CREATE POLICY "Allow wa messages all"
    ON wa_messages FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);


INSERT INTO storage.buckets (id, name, public)
VALUES ('message-assets', 'message-assets', true)
ON CONFLICT (id) DO UPDATE SET public = true;

DROP POLICY IF EXISTS "Public read message assets" ON storage.objects;
CREATE POLICY "Public read message assets"
    ON storage.objects
    FOR SELECT
    USING (bucket_id = 'message-assets');

DROP POLICY IF EXISTS "Allow message assets upload" ON storage.objects;
CREATE POLICY "Allow message assets upload"
    ON storage.objects
    FOR INSERT TO anon, authenticated
    WITH CHECK (bucket_id = 'message-assets');


