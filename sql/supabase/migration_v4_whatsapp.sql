CREATE TABLE IF NOT EXISTS automation_jobs (
    id                     bigint generated by default as identity PRIMARY KEY,
    created_at             timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    updated_at             timestamptz DEFAULT timezone('utc', now()) NOT NULL,
    channel                text NOT NULL,
    trigger_event          text,
    phone_number           text,
    caller_name            text,
    template_name          text,
    message_type           text DEFAULT 'text' NOT NULL,
    body_text              text DEFAULT '' NOT NULL,
    caption                text DEFAULT '' NOT NULL,
    media_url              text,
    mime_type              text,
    file_name              text,
    asset_id               bigint,
    related_call_room_id   text,
    related_call_log_id    bigint,
    related_appointment_id bigint,
    scheduled_for          timestamptz NOT NULL,
    status                 text DEFAULT 'pending' NOT NULL,
    retry_count            integer DEFAULT 0 NOT NULL,
    max_retries            integer DEFAULT 3 NOT NULL,
    last_error             text,
    idempotency_key        text NOT NULL,
    payload                jsonb DEFAULT '{}'::jsonb NOT NULL,
    provider_message_id    text,
    dispatch_id            text,
    CONSTRAINT automation_jobs_channel_check CHECK (channel IN ('whatsapp', 'call')),
    CONSTRAINT automation_jobs_status_check CHECK (
        status IN ('pending', 'retry', 'processing', 'sent', 'failed', 'cancelled', 'pending_review', 'launched')
    )
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_automation_jobs_idempotency
    ON automation_jobs (idempotency_key);
CREATE INDEX IF NOT EXISTS idx_automation_jobs_schedule_status
    ON automation_jobs (status, scheduled_for);
CREATE INDEX IF NOT EXISTS idx_automation_jobs_phone
    ON automation_jobs (phone_number);
CREATE INDEX IF NOT EXISTS idx_automation_jobs_appointment
    ON automation_jobs (related_appointment_id);

